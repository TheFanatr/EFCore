        public CosmosStateManager([NotNull] StateManagerDependencies dependencies) : base(dependencies)
        {
        }

        /// <summary>
        ///     This is an internal API that supports the Entity Framework Core infrastructure and not subject to
        ///     the same compatibility standards as public APIs. It may be changed or removed without notice in
        ///     any release. You should only use it directly in your code with extreme caution and knowing that
        ///     doing so can result in application failures when updating to a new Entity Framework Core release.
        /// </summary>
        public override void StateChanging(InternalEntityEntry entry, EntityState newState)
        {
            base.StateChanging(entry, newState);

            if (entry.EntityType.GetProperties().FirstOrDefault(property => property.Name == StoreKeyConvention.DefaultIdPropertyName) is { } idProperty && entry.GetCurrentValue(idProperty) is null)
            {
                ValueGenerationManager.Generate(entry);
            }
        }
    }
